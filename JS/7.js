// Area of Circle
// pi 3.14
// pi*r*r
// pi*(r**2)

exports.areaOfCircle = function (r){
    const pi = 3.14;
    return pi*r*r
}
// Area of Square
exports.areaOfSquare = function (width, height){
    return width*height
}
// Area of Triangle
exports.areaOfTriangle = function (base, height){
    return 1/2*(base*height)
}
